words_calc = {
    'один': '1', 
    'раз': '1', 
    'два': '2', 
    'три': '3', 
    'четыре': '4', 
    'пять': '5', 
    'шесть': '6',
    'семь': '7',
    'восемь': '8',
    'девять': '9',
    'ноль': '0',
    'плюс': "+",
    'минус': '-',
    'умножить': '*',
    'разделить': '/',
    'и': '.',
    'точка': '.'
}


def to_text_calc(text):
    result = ''
    words = text.split(' ')
    for word in words:
        result += words_calc.get(word,'')
    return result

